function v = Re_to_b_Quaternion(q, v)
            q_0 = q(1);
            q_1 = q(2);
            q_2 = q(3);
            q_3 = q(4);
            v = [1-2*(q_3^2+q_2^2),	2*(q_0*q_3+q_1*q_2),	2*(q_1*q_3-q_0*q_2);
                2*(q_1*q_2-q_0*q_3),	1-2*(q_3^2+q_1^2),	2*(q_2*q_3+q_0*q_1);
                2*(q_1*q_3+q_0*q_2),	2*(q_2*q_3-q_0*q_1),	1-2*(q_2^2+q_1^2)] * v;
end